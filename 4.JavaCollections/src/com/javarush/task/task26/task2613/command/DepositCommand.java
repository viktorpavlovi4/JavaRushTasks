package com.javarush.task.task26.task2613.command;

import com.javarush.task.task26.task2613.CashMachine;
import com.javarush.task.task26.task2613.ConsoleHelper;
import com.javarush.task.task26.task2613.CurrencyManipulator;
import com.javarush.task.task26.task2613.CurrencyManipulatorFactory;
import com.javarush.task.task26.task2613.exception.InterruptOperationException;

import java.util.ResourceBundle;

class DepositCommand implements Command {
    private ResourceBundle res = ResourceBundle.getBundle(CashMachine.RESOURCE_PATH + "deposit_en");

    @Override
    public void execute() throws InterruptOperationException {
        ConsoleHelper.writeMessage(res.getString("before"));
        String currencyCode = ConsoleHelper.askCurrencyCode();
        CurrencyManipulator currencyManipulator = CurrencyManipulatorFactory.getManipulatorByCurrencyCode(currencyCode.toUpperCase());
        String[] s = ConsoleHelper.getValidTwoDigits(currencyCode.toUpperCase());
        currencyManipulator.addAmount(Integer.parseInt(s[0]), Integer.parseInt(s[1]));
        ConsoleHelper.writeMessage(String.format(res.getString("success.format"), Integer.parseInt(s[0]) * Integer.parseInt(s[1]), currencyManipulator.getCurrencyCode()));

    }
}